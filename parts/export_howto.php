    <h1>Detaildaten der Gewinn und Verlust-Rechnung exportieren</h1>
    <p>
    Um die Gewinn- und  Verlust-Rechnung nach Monaten zu exportieren gehen Sie wie folgt vor:
    </p>
    <ol>
    <li>Wählen Sie im Hauptmenü des Haushaltsbuchs den Eintrag <b>Auswertungen</b>:</li>
    <img src="./img/export_howto_startscreen.png">
    <br/><br/>
    <li>Danach wählen Sie im Menü Auswertungen den Eintrag <b>GuV Monate exportieren</b> auswählen.</li>
    <img src="./img/export_howto_auswertung.png">
    <br/><br/>
    <li>Öffnen Sie die heruntergeladene Datei mit Doppelklick mit Excel oder Calc</li>
    <h3>Detaildaten der Gewinn und Verlust-Rechnung nach LibreOffice Calc importieren</h3>
    <li>Beim öffnen mit Calc öffnet sich dann der folgende Datenimport-Dialog</li>
    <img src="./img/export_howto_importieren.png">
    <br/><br/>
    <li>Das Ergebnis des Imports ist eine Tabelle der Monatssalden der Ertrags- und Aufwandskonten.</li>
    <img src="./img/export_howto_importiert.png">
    <br/>
    <h3>Als Pivot-Tabelle in LibreOffice Calc auswerten</h3>
    <p>
    Die so importierten Daten eignen sich besonders gut für eine Auswertung als Pivot-Tabelle. Dazu gehen Sie wie folgt vor.
    </p>
    <li>Klicken Sie mit er Maus in eine Zelle der importierten Daten</li>
    <li>Danach wählen Sie im Menü Daten &gt; Pivot-Tabelle &gt; Erstellen</li>
