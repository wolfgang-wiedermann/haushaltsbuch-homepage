    <h1>Vorgeschichte des HTML5-Haushaltsbuchs</h1>
    <p>
    Ich habe im Juli 2013 damit begonnen das HTML5-Haushaltsbuch zu entwickeln, da ich seit Jahren mit dem Problem
    konfrontiert war, dass ich durch meine zwei Wohnsitze häufiger an klassischer Haushaltsbuchführung gescheitert
    bin und das obwohl das vorher mit nur einem Wohnort und einer schnell programmierten Desktop-Anwendung 
    einwandfrei funktioniert hatte. Mein Problem war also, dass ich ein System zur Orts- und Hardware-unabhängigen
    Erfassung meiner Buchungen benötigte. Dazu erschien mir eine offlinefähige HTML5-App mit serverseitiger Speicherung
    der Buchungen als besonders geeignet, da ich so bei den mobilen Endgeräten nicht an ein Betriebssytem (iOS oder Android)
    gebunden bin. In der Praxis zeigte sich aber auch schnell, dass der Vorteil noch größer war, denn ich konnte
    die App natürlich auch von beliebigen Rechner aus verwenden.
    </p>
    <p>
    Programmiert habe ich das HTML5-Haushaltsbuch dann immer an den Wochenenden Abends während des Fernsehens.
    Ziel der Entwicklung war immer vorrangig ich selbst. Im Laufe der Zeit stellte sich allerdings heraus, dass auch
    andere Interesse an dieser Software hatten. Die erste weitere Nutzerin des HTML5-Haushaltsbuchs war dann
    meine eigene Schwester, die durch fundierte Kritik auch einiges zu dessen Funktionsumfang beigetragen hat.
