        <h1>Wie gestalte ich einen eigenen Kontenplan?</h1>
        <p>
        Vordefinierte Kontenpläne oder Kategorien sind praktisch, bequem und verursachen wenig Aufwand.
        Deshalb ist die Frage, weshalb man sich den Aufwand machen sollte, einen eigenen Kontenplan zu
        entwerfen durchaus ligitim.
        </p>
        <p>
        Vordefinierte Pläne orientieren sich allerdings nicht an den individuellen Sparzielen und 
        berücksichtigen keine individuellen Rahmenbedingungen wie Mehr-Personen-Haushalte oder
        Nebenjobs, Nebeneinkommen aus Land- und Forstwirtschaft, Einkünfte aus Beratungstätigkeit 
        und vieles mehr.
        </p>
        <p>
        Deshalb gilt folgende Faustregel:
        </p>
        <p style="font-weight:bold">
        Wenn Sie die Effektivität Ihrer verschiedenen Erwerbsarten ermitteln oder gezielt zwischen
        für Sie wichtigen und unwichtigeren Ausgabenarten unterscheiden wollen ist ein eigener
        Kontenplan unumgänglich.
        <p>
        <p>
        Die folgende Anleitung beschreibt, wie Sie Schritt für Schritt einen individuell an Ihre
        persönlichen Bedürfnisse angepassten Kontenplan entwickeln.
        </p>
    </div>
    <div>
    <h1>Schritt 1: Stoffsammlung</h1>
    <p>
    In Schritt 1 sammeln Sie alle Konten-Kandidaten, die Ihnen einfallen.<br/> 
    Typische Konten-Kandidaten sind:
    <ol>
      <li>Alle Arten von Eigentum, die Sie haben. (z. B. Grundstücke, Autos, Häuser, Werkzeuge, Computer etc.)</li>
      <li>Alle Arten von Verbindlichkeiten (Schulden), die Sie haben. (Bankdarlehen etc.)</li>
      <li>Alle Arten von Ausgaben bzw. Aufwendungen die Sie üblicherweise haben. (Miete, Lebensmittel, Versicherungen etc.)</li>
      <li>Alle Arten von Einnahmen, die Sie üblicherweise haben. (Gehalt, Nebentätigkeit A, Rückzahlungen vom Finanzamt, Geschenke etc.)</li>
    </ol>
    Schreiben Sie alle Punkte die Ihnen hierzu einfallen auf einen Zettel oder in eine Textdatei. Bei den Ausgaben und Einnahmen hilft
    es Ihnen eventuell, wenn Sie die Kontoauszüge des vergangenen Jahres betrachten. Aber Achtung, nicht jede Ausgabeart wird auf Ihrem
    Bankkonto ersichtlich. Berücksichtigen Sie auch jene Ausgaben, die Sie aus Ihrem Geldbeutel tätigen.
    </p>
    </div>
    <div>
    <h1>Schritt 2: Gruppieren verwandter Elemente</h1>
    Im nächsten Schritt bringen Sie einen ersten Hauch von Struktur in Ihre lose Sammlung. Dazu bilden Sie auf zwei verschiedene Arten
    Gruppen verwandter Elemente.
    <h2>Gruppierungskonzept 1: Global zusammenhängendes</h2>
    Diese Gruppierung dient primär dazu, Ihnen einen verbesserten Überblick über die Zusammenhänge innerhalb Ihrer Sammlung von Konten-Kandidaten
    zu verschaffen. Hier versuchen Sie bitte alle Dinge die in einem direkten Zusammenhang stehen zu gruppieren. Bringen Sie z. B. Ihr Gehalt,
    Ihre Lebenshaltung zusammen und stellen Sie Nebeneinkommen in den Kontext der durch die entsprechende Nebentätigkeit entstehenden Kosten bzw.
    Aufwendungen.<br/>
    <br/>
    <u>Beispiel:</u>
    Besitzen Sie z. B. Wald, so stellen Sie die Aufwendungen wie z. B. die Beiträge zur Berufsgenossenschaft, Kraftstoff für Werkzeuge, Verschleißteile
    den Erlösen aus dem Verkauf verschiedener Holzarten (Brennholz, Industrieholz, etc.) gegenüber.<br/>
    <br/>
    Werden Sie gelegentlich als IT-Berater tätig, so können Sie Ihre dafür genutzte Hardware, Literatur sowie die anfallenden Fahrkosten Ihren aus
    den Beratungsleistungen entstandenen Einkünften gegenüberstellen.<br/>
    <br/>
    Wenn Sie diese Zusammenhänge bereits in dieser frühen Phase der Planung Ihres Kontenplans berücksichtigen schaffen Sie eine Buchungssystematik,
    die ein gezieltes Controlling Ihrer verschiedenen Tätigkeiten ermöglicht.
    <h2>Gruppierungskonzept 2: Zusammenhängendes nach Kontenarten</h2>
    Das HTML5-Haushaltsbuch unterstützt 4 grundlegende Kostenarten. Sie lassen sich auf die klassische Kaufmännische Buchführung zurückführen.<br/>
    <br/>
    Diese Kontenarten sind:
    <ul>
      <li>Aktiv-Konten = Bestandskonten: Beispiele: Grundstück A, Grundstück B, Auto, Motorsäge, Fräsmaschine, Girokonto, Bausparer, Geldbeutel</li>
      <li>Passiv-Konten (beschreiben Kapitalstruktur): Darlehen, kurzfristige Kredite, Eigenkapital</li>
      <li>Aufwandskonten: Beispiel: Miete, Nebenkosten Wohnung, Kraftstoff fürs Auto, Strom, Telefon und Internet, Literatur, Gaststättenbesuche, Luxusartikel</li>
      <li>Ertragskonten: Beispiel: Gehalt, Nebeneinkommen aus Lehrtätigkeit, Nebeneinkommen 400-Euro-Job, Geschenke, Zinsen, Steuerrückerstattungen</li>
    </ul>
    Ordnen Sie im nächsten Schritt Ihre Kontenkandidaten den entsprechenden Kontenarten zu und sortieren Sie diese möglichst nach sinnvollen Obergruppen.
    Typische Kandidaten für solche Gruppen wären z. B. Essen, Wohnen, Mobilität, Kommunikation, Sonstiges.
    </div>
    <div>
    <h1>Schritt 3: Kontonummern</h1>
    In diesem Schritt bilden Sie ein geeignetes Nummernsystem für Ihre Konten. 
    <b>Die Anleitung für diesen Schritt wird in Kürze ergänzt.</b>
    </div>
    <div>
    <h1>Schritt 4: Anlegen der Konten</h1>
    Zum Anlegen der Konten verwenden Sie bitte die entsprechende Maske im HTML5-Haushaltsbuch. Sie erreichen diese über den Menü-Eintrag "Konten" im 
    Hauptmenü. In der zugehörigen Maske können Sie neue Konten anlegen oder bestehende bearbeiten. <br/>
    <br/>
    <b>Bitte beachten Sie folgendes: Es ist nicht möglich einmal angelegte Konten zu löschen oder deren Kontonummern zu verändern!</b>
    </div>
</div>